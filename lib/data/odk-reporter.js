const { v4: uuid } = require('uuid');
const { encodeXML } = require('entities');

const metaWithUuidXml = () => {
  const thisUuid = uuid();
  return `<meta><instanceID>uuid:${thisUuid}</instanceID></meta>`;
};

const convertObjectToXml = (data) => {
  // Takes form submission data (e.g., of analytics metrics)
  // representated as an Object and turns it into the meat
  // of a form XML submission.

  // Values may contain user-generated content (e.g., organization name or email address)
  // and are XML-encoded for safety. Object keys are assumed safe and not encoded,
  // as they are generated by the application, not users.

  if (data == null) return '';

  if (typeof data === 'object') {
    let output = '';
    for (const [k, v] of Object.entries(data)) {
      if (Array.isArray(v)) {
        // If the data is an array, it is repeat data and
        // the xml should have the outer tag repeated e.g.
        // <projects><num_users>5</num_users></projects><projects><num_users>44</num_users></projects>
        for (const repeat of v) {
          output = output.concat(`<${k}>`, convertObjectToXml(repeat), `</${k}>`);
        }
      } else {
        output = output.concat(`<${k}>`, convertObjectToXml(v), `</${k}>`);
      }
    }
    return output;
  } else {
    return encodeXML(String(data));
  }
};

const buildSubmission = (formId, formVersion, data) => {
  const innerXml = convertObjectToXml(data);
  const metaXml = metaWithUuidXml();
  const dataPreamble = 'xmlns:jr="http://openrosa.org/javarosa" xmlns:orx="http://openrosa.org/xforms"';
  return `<?xml version="1.0"?><data ${dataPreamble} id="${encodeXML(formId)}" version="${encodeXML(formVersion)}">${innerXml}${metaXml}</data>`;
};

module.exports = {
  buildSubmission,
  convertObjectToXml,
  metaWithUuidXml
};

